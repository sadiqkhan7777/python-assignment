# 2. Assignment Operators
# Used to assign values to variables.

x = 5  # Assigning value
x += 3  # Equivalent to x = x + 3
print(x)  # 8

x -= 2  # Equivalent to x = x - 2
print(x)  # 6

x *= 4  # Equivalent to x = x * 4
print(x)  # 24

x /= 3  # Equivalent to x = x / 3
print(x)  # 8.0

x %= 5  # Equivalent to x = x % 5
print(x)  # 3.0

x **= 2  # Equivalent to x = x ** 2
print(x)  # 9.0

x //= 2  # Equivalent to x = x // 2
print(x)  # 4.0
