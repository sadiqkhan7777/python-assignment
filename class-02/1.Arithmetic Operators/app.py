# 1. Arithmetic Operators
# These operators perform mathematical operations.

a = 10
b = 3

# Addition
print(a + b)  # 13

# Subtraction
print(a - b)  # 7

# Multiplication
print(a * b)  # 30

# Division
print(a / b)  # 3.3333

# Floor Division (removes decimal part)
print(a // b)  # 3

# Modulus (remainder)
print(a % b)  # 1

# Exponentiation (power)
print(a ** b)  # 10^3 = 1000
